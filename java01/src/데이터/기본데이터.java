package 데이터;

public class 기본데이터 {

	public static void main(String[] args) {
		//기본데이터 4가지
		//정수, 실수, 문자1, 논리
		//문자1(char), 문자여러개(String)
		int age = 100; 
		double eye = 1.5;
		char gender = '남'; 
		boolean food = true;
		
		//컨트롤+스페이스바: 자동완성
		//sysout, syso
		System.out.println("내 나이는" + age);
		//컨틀로 + f11 :실행
		System.out.println("내 시력은" + eye);
		System.out.println("나의 성별은" + gender);
		System.out.println("점심은" + food);
	}

}
